    private void tampilkan(){
        int row = this.tabelDataMember.getRowCount();
        for(int a=0; a<row; a++){
            model.removeRow(0);
        }
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/posdb", "root", "");
            ResultSet rs =cn.createStatement().executeQuery("SELECT * FROM datamember");
            while(rs.next()){
                String data [] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)};
                model.addRow(data);
            }
        } catch (SQLException ex){
            Logger.getLogger(dataMember.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


