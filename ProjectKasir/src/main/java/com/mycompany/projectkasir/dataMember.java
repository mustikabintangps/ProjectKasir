    private void tblSimpanMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblSimpanMemberActionPerformed
        // TODO add your handling code here
        
        String nama = inputNama.getText();
        String alamat = inputAlamat.getText();
        String noHP = inputNoHp.getText();
        String jenisKelamin = (String) inputJenisKelamin.getSelectedItem();

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/posdb", "root", "");
            String query = "INSERT INTO datamember (nama, alamat, noHP, jenisKelamin) VALUES (?,?,?,?)";
            PreparedStatement ps = conn.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
            ps.setString(1, nama);
            ps.setString(2, alamat);
            ps.setString(3, noHP);
            ps.setString(4, jenisKelamin);
            
            int result = ps.executeUpdate();
            
            if (result > 0) {
                
                ResultSet generatedKeys = ps.getGeneratedKeys();
                
                if(generatedKeys.next()){
                    int generatedID = generatedKeys.getInt(1);
                    
//                    JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
                    tampilkan();
                    
                    this.kartuMember.setVisible(true);
                    this.labelShowIDMember.setText(Integer.toString(generatedID));
                    this.labelShowNamaMember.setText(nama);
                    this.labelShowJenisKelaminMember.setText(jenisKelamin);
                    this.kartuMember.setSize(500, 325);
                }     
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data");
            }
            conn.close();
            ps.close();
        } catch (Exception ex) {
            Logger.getLogger(dataBarang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblSimpanMemberActionPerformed

    private void tblHapusMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblHapusMemberActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelDataMember.getSelectedRow();
        
        if (selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus!");
        } else {
            String idToDelete = tabelDataMember.getValueAt(selectedRow, 0).toString();
            
        model.removeRow(selectedRow);
            
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/posdb", "root", "");
            String query = "DELETE FROM datamember WHERE id = ?";
            try (PreparedStatement ps = cn.prepareStatement(query)){
                ps.setString(1, idToDelete);
                int result = ps.executeUpdate();
                if (result >0){
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                    tampilkan();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data.");
                }
                ps.close();
           }
           cn.close();
        }catch (SQLException ex){
            Logger.getLogger(dataMember.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghubungkan ke database.");
        }
        }
    }//GEN-LAST:event_tblHapusMemberActionPerformed

